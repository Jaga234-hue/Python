import numpy as np
print("Zero Matrix");
print(np.zeros((2, 3)));
print("One Matrix");
print(np.ones((3, 2)));
print("Identity Matrix");
print(np.eye(3));
print("filled with 5 Matrix");
print(np.full((2,2), 5));
print("numbers from 1 to 10 with step 2 Matrix");
print(np.arange(1, 10, 2));
print("5 equally spaced numbers between 1 and 10 Matrix");
print(np.linspace(1, 10, 5));